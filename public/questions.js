let selectedCourse = "";
let questions = [];
let questionIndex = 0;
let userScore = 0;

const startBtn = document.getElementById("start-quiz-btn");
const courseSelect = document.getElementById("course-select");
const quizSection = document.querySelector(".quiz-section");
const questionText = document.querySelector(".question-text");
const optionsList = document.querySelector(".option-list");
const nextBtn = document.querySelector(".next-btn");
const totalQuestionEls = document.querySelectorAll(".total-questions");
const currentQuestionEl = document.querySelector(".current-question");
const currentScore = document.querySelector(".current-score");
const courseLabel = document.getElementById("selected-course-label");

startBtn.addEventListener("click", () => {
  selectedCourse = courseSelect.value;

  if (!selectedCourse) {
    alert("Please select a course.");
    return;
  }

  questions = getRandomQuestionsFromCourse(selectedCourse);

  if (questions.length === 0) {
    alert("No questions available for this course.");
    return;
  }

  document.getElementById("home-section").style.display = "none";
  quizSection.style.display = "block";

  courseLabel.textContent = selectedCourse.toUpperCase();
  questionIndex = 0;
  userScore = 0;

  totalQuestionEls.forEach(el => el.textContent = questions.length);

  showQuestion(questionIndex);
  updateProgressBar(questionIndex);
});



function getRandomQuestionsFromCourse(course) {
  const filtered = questionBank.filter(q => q.course === course);
  const shuffled = [...filtered].sort(() => Math.random() - 0.5); // optional: shuffle
  return shuffled; // no slicing = all questions returned
}




function showQuestion(index) {
  const q = questions[index];
  questionText.textContent = `${index + 1}. ${q.question}`;
  optionsList.innerHTML = "";

  q.options.forEach(option => {
    const div = document.createElement("div");
    div.className = "option";
    div.innerHTML = `<span>${option}</span>`;
    div.onclick = () => selectOption(div, q.answer);
    optionsList.appendChild(div);
  });

  currentQuestionEl.textContent = index + 1;
  currentScore.textContent = userScore;
  updateProgressBar(index);
}

function selectOption(selected, correctAnswer) {
  const userAnswer = selected.textContent;
  const options = optionsList.children;

  if (userAnswer === correctAnswer) {
    selected.classList.add("correct");
    userScore++;
  } else {
    selected.classList.add("incorrect");
    [...options].forEach(opt => {
      if (opt.textContent === correctAnswer) {
        opt.classList.add("correct");
      }
    });
  }

  [...options].forEach(opt => opt.classList.add("disabled"));
  nextBtn.classList.add("active");
}

nextBtn.onclick = () => {
  questionIndex++;
  if (questionIndex < questions.length) {
    showQuestion(questionIndex);
    nextBtn.classList.remove("active");
  } else {
    alert(`Quiz complete! Your score is ${userScore} / ${questions.length}`);
    nextBtn.classList.remove("active");
  }
};

function updateProgressBar(index) {
  const progressBar = document.getElementById("progress-bar");
  const percentage = ((index + 1) / questions.length) * 100;
  progressBar.style.width = `${percentage}%`;
}

